package rebu.model;

public class Corrida {
	
	private int idCorrida;
	private int idMotCorrida;
	private int idPasCorrida;
	private double vlCorrida;

	public int getIdCorrida() {
		return idCorrida;
	}

	public void setIdCorrida(int idCorrida) {
		this.idCorrida = idCorrida;
	}

	public int getIdMotCorrida() {
		return idMotCorrida;
	}

	public void setIdMotCorrida(int idMotCorrida) {
		this.idMotCorrida = idMotCorrida;
	}

	public int getIdPasCorrida() {
		return idPasCorrida;
	}

	public void setIdPasCorrida(int idPasCorrida) {
		this.idPasCorrida = idPasCorrida;
	}

	public double getVlCorrida() {
		return vlCorrida;
	}

	public void setVlCorrida(double vlCorrida) {
		this.vlCorrida = vlCorrida;
	}
}
